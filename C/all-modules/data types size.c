#include<stdio.h>

int main()
{
    // integers
    printf("%d \n", sizeof(int));
    printf("%d \n", sizeof(long int));
    printf("%d \n", sizeof(long long int));

    // float
    printf("%d \n", sizeof(float));
    printf("%d \n", sizeof(double));

    // character
    printf("%d \n", sizeof(char));

    return 0;
}
